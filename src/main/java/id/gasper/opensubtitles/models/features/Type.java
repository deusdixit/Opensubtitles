package id.gasper.opensubtitles.models.features;

/**
 * Represents a type of media feature commonly used in the context of subtitles or other media-related metadata.
 * The type can be one of the following:
 * - MOVIE: Represents a feature of type movie.
 * - TVSHOW: Represents a feature of type TV show.
 * - EPISODE: Represents a feature of type episode, typically part of a TV show.
 */
public enum Type {
    MOVIE,
    TVSHOW,
    EPISODE
}
